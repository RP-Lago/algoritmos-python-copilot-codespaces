# Agora vamos solicitar ao usuário que digite um texto e um número inteiro. e posteriormente repetir o texto a quantidade de vezes que o número inteiro informado pelo usuário.

# Solicitando ao usuário que digite um texto e um número inteiro
texto = input("Digite um texto:")
numero = int(input("Digite um numero inteiro: "))

# Repetindo o texto
print((texto +" ") * numero)

